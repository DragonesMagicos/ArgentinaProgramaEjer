package exeption;

public class calculadora {
	
	public static double suma (double a , double b) {
		
		return a+b;
		
		
	}
	
	public static double resta (double a,double b) {
		return a-b;
	}
	
	public static  double mul (double a,double b) {
		return a*b;
	}
	
	public static  double div (double a,double b) {
		return a/b;
	}

}
